    private void fillcomboBox(){
        
        
        
        
        try {
            PreparedStatement stmt=con.prepareCall("select doctor_id,doctor_specialty from doctors ");
            
            ResultSet rs=stmt.executeQuery();
             
            while(rs.next()){
               doctor_id.add(rs.getInt(1));
            jComboBox2.addItem(rs.getString(2));
            
            }
            
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(patient_info.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    
    
    }
    
    
    
