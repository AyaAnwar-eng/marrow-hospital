        // TODO add your handling code here:
        appointment x=new appointment();
        x.setVisible(true);
        
        if (jTextField5.getText().length() > 0 && jTextField3.getText().length() > 0&&jTextField4.getText().length() > 0&&jTextField1.getText().length() > 0) {
            try {
                String name = jTextField5.getText();
                String id = jTextField3.getText();
                String age=jTextField4.getText();
                String phone=jTextField1.getText();
                
                String gender;
                if (jRadioButton1.isSelected()) {
                    gender = "Male";
                } else {
                    gender = "Female";
                }
                String case_patient = jComboBox2.getSelectedItem().toString();
                String ms = jComboBox1.getSelectedItem().toString();
                
                PreparedStatement stmt =con.prepareStatement("insert into patient(patient_id,patient_name,patient_phone,gender,patient_case,age,matridl_status)values(?,?,?,?,?,?,?)");
                 stmt.setString(1, id);
                 stmt.setString(2, name);
                 stmt.setString(3, phone);
                 stmt.setString(4, gender);
                 stmt.setString(5, age);
                 stmt.setInt(6, doctor_id.get(jComboBox2.getSelectedIndex()));
                 stmt.setString(7, ms);
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Appointment is successful");
                
                
                
            } catch (SQLException ex) {
                Logger.getLogger(patient_info.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        }
        
